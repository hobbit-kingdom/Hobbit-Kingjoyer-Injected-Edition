// ===========================================================================
//  1. LightningFork — nested weather_mgr::fork (one bolt branch).
//     Allocated 0x14 bytes by GetUnused*Fork; a 0x45c-byte geometry block hangs
//     off +0x4 (segment vertex buffer). Cast a fork* to this.
// ===========================================================================
// The ctor (0x00551040) writes byte flags at +0x0d, +0x0f, +0x11, +0x13 and dwords
// at +0x4 / +0x8; the 0x45c geometry block holds the segment-count short at its +0x2
// and the vertex array (stride 0xc) referenced by RenderFork. textureA/textureB
// handles for RenderFork are read from the geometry block (+0xc/+0x10), not the
// fork header. Byte-flag meanings are partly inferred ("?").
#pragma pack(push, 1)
struct LightningFork {
    int16_t  lifetime;                 // 0x00  frames remaining (>0 = live); decremented in BuildFork
    uint8_t  _pad02[0x04 - 0x02];      // 0x02
    void*    geometry;                 // 0x04  -> 0x45c segment/geometry block (seg count short @ +0x2)
    void*    handle08;                 // 0x08  secondary geometry/buffer handle (freed by fork::Free) "?"
    uint8_t  _pad0c[0x0d - 0x0c];      // 0x0c
    uint8_t  flag0d;                   // 0x0d  flag byte (ctor = 0)
    uint8_t  _pad0e;                   // 0x0e
    uint8_t  alpha;                    // 0x0f  fade/alpha byte (ctor = 0xff; BuildFork writes __ftol(color.a))
    uint8_t  _pad10;                   // 0x10
    uint8_t  alpha11;                  // 0x11  secondary alpha byte (ctor = 0xff) "?"
    uint8_t  _pad12;                   // 0x12
    uint8_t  flag13;                   // 0x13  cleared (=0) each BuildFork update; ctor = 0xff
};                                     // size 0x14
#pragma pack(pop)

// ===========================================================================
//  2. WeatherMgr — global weather state singleton (engine weather_mgr).
//     The live instance is the engine global g_WeatherMgr; cast its address.
//     Do NOT instantiate.
// ===========================================================================
// stormEffectActive(0x58) is immediately followed by stormEffectBase(0x5c), so
// there is NO padding between them (the gap would be zero-length and is omitted).
#pragma pack(push, 1)
struct WeatherMgr {
    uint8_t  _pad000[0x10 - 0x00];     // 0x000  base/header (SetDefaults memclears 0x10 bytes)
    float    stormIntensity;           // 0x010  rain/storm intensity; fades when < 0.001
    float    rainEffectScale;          // 0x014  rain particle effect scale (default 1.0; Save/RestoreState)
    uint8_t  _pad018[0x50 - 0x18];     // 0x018
    void*    weatherSystemHandle;      // 0x050  particle-weather system handle (FUN_0065a050/FUN_00659c80)
    uint8_t  stateFlags;               // 0x054  WeatherFlag bitfield (bit0 storm, bit1 particle)
    uint8_t  _pad055[0x58 - 0x55];     // 0x055
    int32_t  stormEffectActive;        // 0x058  secondary storm-effect active flag (Save/RestoreState)
    float    stormEffectBase;          // 0x05c  base of the +0x5c..0x60 storm effect (FUN_005893e0)
    float    stormEffectScale;         // 0x060  storm-effect intensity (default 1.0; restored to 1.0)
    uint8_t  _pad064[0x6c - 0x64];     // 0x064
    int32_t  renderDrawCount;          // 0x06c  Render resets to 0 (accumulator)
    void*    stormRenderBuffer;        // 0x070  storm volume render buffer/handle (freed when slots==0)
    int32_t  stormRenderCount;         // 0x074  Render clamps to >= 10; storm-volume capacity target
    int32_t  stormActiveSlots;         // 0x078  active storm-volume slots (0 => free render buffer)
    uint8_t  _pad07c[0x84 - 0x7c];     // 0x07c
    int32_t  stormEffectSelector;      // 0x084  storm/ambient effect active selector (Save/RestoreState)
    int32_t  lightningTtl;             // 0x088  lightning-flash lifetime; PreSceneRender counts down
    void*    lightningEffectHandle;    // 0x08c  EnableStorm: FUN_00647b10(0,6) effect handle
    float    lightningRadius;          // 0x090  EnableStorm sets 2.0 (also base of FUN_005893e0)
    vector3  stormCenter;              // 0x094  storm world position (EnableStorm copies param center)
    uint8_t  stormColor[4];            // 0x0a0  storm RGBA tint (EnableStorm copies param color)
    uint8_t  _pad0a4[0xa8 - 0xa4];     // 0x0a4
    uint8_t  forkCount;                // 0x0a8  primary fork count (BuildFork loop bound)
    uint8_t  secondaryForkCount;       // 0x0a9  secondary fork count (GetUnusedSecondaryFork cap)
    uint8_t  _pad0aa[0xac - 0xaa];     // 0x0aa
    void**   forks;                    // 0x0ac  primary fork pointer array (0x80 bytes = 32 ptrs)
    void**   secondaryForks;           // 0x0b0  secondary fork pointer array (0x800 bytes = 512 ptrs)
};                                     // size >= 0xb4 (singleton; trailing fields beyond 0xb4 unmapped)
#pragma pack(pop)

