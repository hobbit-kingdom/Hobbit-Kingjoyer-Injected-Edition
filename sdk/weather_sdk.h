// ============================================================================
//  weather_sdk.h  —  WEATHER / SKY / ATMOSPHERE SDK for THE HOBBIT (2003) PC build
//  Entropy engine, MSVC, 32-bit (Meridian.exe, preferred base 0x00400000, ASLR off).
//
//  Reverse-engineered modding / trainer SDK. All addresses are absolute PC
//  virtual addresses; resolve them at runtime with meridian::rebase(0xADDR),
//  which applies the current module-base delta vs the analyzed image base.
//
//  This subsystem is the engine's environmental-atmosphere layer:
//    * weather_mgr  — global singleton (g_WeatherMgr) that owns the live weather
//                     state. Per frame OnAdvanceLogic ramps/fades the rain & storm
//                     intensity, advances the particle-weather system, recomputes
//                     a storm AABB around the camera, and lets UI "Storm_" marker
//                     windows seed localized lightning storms. PreSceneRender (run
//                     each frame from render_mgr::Render) strobes the lightning
//                     flash light, and Render submits the storm volumes. Lightning
//                     bolts are recursive fractal "forks" (primary + secondary
//                     pools) generated by TriggerLightningChain.
//    * skybox       — sky-dome object (derives from rigid_instance). Renders first
//                     as the background, scrolls/animates via AnimSpeed +
//                     OffsetScale, and is forced non-occluding / non-colliding.
//    * fog_plane    — planar/distance fog quad (derives from wall). Four corner
//                     vertices + up to N animated UV-scrolling texture layers
//                     drawn in RenderCustom; OnAdvanceLogic advances the UV phase.
//
//  Field offsets are CONSOLIDATED from the per-chunk analysis (weather__00.json)
//  and the decompiled bodies. Offsets marked "?" are lower confidence. Only well-
//  evidenced fields are named; the rest is explicit byte padding so named members
//  land at their true byte offsets. These structs are NOT meant to be
//  instantiated — only reinterpret_cast onto a live engine object (the
//  g_WeatherMgr singleton, or a skybox/fog_plane slot from obj_mgr).
//
//  ---- USAGE / RECIPE ------------------------------------------------------
//    using namespace weather_sdk;
//
//    // Grab the live weather manager singleton (engine global g_WeatherMgr @ 0x00771B70):
//    WeatherMgr* wm = GetWeatherMgr();
//
//    // Force heavy rain (enable flag + fade seconds). NOTE: call on the GAME's main
//    // thread, while a level is loaded (ideally outdoors) — it loads RainOutdoorLoop
//    // and ramps a sub-object that only exists after the level's weather_mgr::Init.
//    fn::WeatherMgr_EnableRain(wm, 1, 1.0f);   // 1 = on, 0 = off ; 1.0f = fade time
//    // Read / poke the rain intensity directly:
//    wm->stormIntensity = 0.85f;
//
//    // Trigger a localized lightning storm at a point (center, RGBA tint):
//    vector3 where = { x, y, z };
//    uint8_t tint[4] = { 0xff, 0xb4, 0xe6, 0xff };   // R,G,B,A
//    fn::WeatherMgr_EnableStorm(wm, &where, tint);   // plays "LightningCrack"
//
//    // Fire a single lightning bolt between two points (5-arg overload):
//    vector3 a = { ax, ay, az }, b = { bx, by, bz };
//    int forkIdx = fn::WeatherMgr_TriggerLightningChain(wm, a, b, 1.0f, /*branchy*/1, 0);
//
//    // Disable all weather (frees fork pools, stops particle weather):
//    fn::WeatherMgr_Kill(wm);
//
//    // Force a sky preset on a skybox slot (editor-style):
//    Skybox* sky = reinterpret_cast<Skybox*>(skySlotPtr);
//    sky->animSpeed   = 0.0f;                         // freeze sky scroll
//    sky->offsetScale = vector3{ 1.0f, 1.0f, 1.0f };  // dome offset scaling
//
//    // Disable a fog plane (collapse its quad / clear textures): cast to FogPlane,
//    // null the texture handles, or just hide the owning object via the engine.
//
//  __thiscall wrappers take the object pointer as the FIRST argument; the static
//  object factories (SkyboxCreator/FogPlaneCreator) are __cdecl. Methods that take
//  an INCOMPLETE engine type BY VALUE, or return a large aggregate via hidden sret
//  (GetProperty), expose an address accessor (*_at) with the raw signature in a
//  comment instead of a typed wrapper. vector3/radian3/bbox are complete
//  (meridian_types.h) so their by-value signatures are ABI-safe.
//
//  NAMING NOTES:
//    * Engine class `weather_mgr` is modeled here as `WeatherMgr`, `skybox` as
//      `Skybox`, `fog_plane` as `FogPlane`, and the nested `weather_mgr::fork`
//      as `LightningFork`, to keep PascalCase wrapper types distinct from the
//      forward-declared engine structs in meridian_types.h.
//    * Engine type names that collide with the std namespace would be written
//      global-qualified (::internal, ::system, ...) to stay correct under
//      `using namespace std;`. None are referenced by value here.
// ============================================================================
